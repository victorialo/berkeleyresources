</li>
</ul>
<li><strong><a href="http://www.amazon.com/gp/student/signup/info?ie=UTF8&refcust=H2DIDRT7ZPIESATHO72BZ3TNNY&ref_type=generic" target="_blank">Amazon Student aka Amazon Prime</a> </strong> <br />
