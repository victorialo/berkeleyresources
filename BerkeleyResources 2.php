<a name="academic"><h3>Academic:</h3></a>
<ul>
<li><strong><a href="http://bspace.berkeley.edu" target="_blank">bSpace</a></strong>  <br />
Homework, calendars, files, etc. for all of your classes.
</li>
<li><strong>Facebook Group: <a href="https://www.facebook.com/groups/176230269085268/" target="_blank">STUDY TIPS and OTHER USEFUL THINGS</a></strong>  <br />
  A place to find study tips and general tips for Berkeley classes<BR>
<li><strong><a href="https://www.facebook.com/groups/groupsatberkeley/" target="_blank">Find your class year's and major's Facebook group</a></strong><br />
</li>
<li><strong><a href="http://academicservices.berkeley.edu/advising/gpa/" target="_blank">GPA Calculator</a></strong>  <br />
GPA calculator from the ASC. <BR>
</li>
<li><strong><a href="https://www.refworks.com/refworks2/default.aspx?r=authentication::init&groupcode=ucberkeleybears" target="_blank">RefWorks</a></strong><br>
Organize and cite the research papers used in a project/paper in any format.
</li>
<li><strong><a href="http://easybib.com" target="_blank">EasyBib</a></strong><br>
Easily make a bibliography in MLA format. 
</li>
<li><strong><a href="https://tbp.berkeley.edu/students/exams/" target="_blank">Tau Beta Pi Test Bank</a></strong>  <br />
  Old exams from pretty much all STEM classes, both upper- and lower-div. SUPER helpful.<BR>
</li>
<li><strong><a href="https://hkn.eecs.berkeley.edu/exams/" target="_blank">HKN's Test Bank</a></strong>  <br />
  Old exams from all EE & CS classes, both upper- and lower-div. HKN's site also has course guides (descriptions, reviews, etc.) for most EE & CS classes. <BR>
</li>
<li><strong><a href="http://thequad.com/share/75tqFD" target="_blank">Quad</a> </strong> <br />
 Quad inputs all of your assignments/projects using your class's syllabus to create a calendar with due dates. Currently only for Berkeley students. 
</li>
<li><strong><a href="http://oneclass.com/a/146154" target="_blank">OneClass</a></strong><br />
Organize your notes, share class notes with others, and get recommended notes/videos for your class. Also, when you join, you're entered into a <a href="http://oneclass.com/scholarships" target="_blank">scholarship</a> or can win <a href="http://oneclass.com/freetextbooks" target="_blank">free textbooks</a>.  
</li>
<li><strong><a href="http://beta.classmint.com/" target="_blank">Classmint</a> </strong><br />
  An interactive browser-based note-taking tool with text and image annotations.<BR>
</li>
<li><strong><a href="http://s.tudy.it/twkpdup" target="_blank">StudyBlue</a></strong><br>
Make and share flashcards accessible by computer and smartphone for all of your classes. (Get a free month of Pro with the link above.)
</li>
<li><strong><a href="http://uguru.me/r/jwt7c" target="_blank">Uguru.me</a> </strong> <br />
Tutor someone in a Berkeley class or a skill to earn some money.  
</li>
<li><strong><a href="http://writtenkitten.net/" target="_blank">Written? Kitten!</a> </strong> <br />
Need some motivation to start writing that paper? Every 100 words, you'll be rewarded with a new kitten.
</li>
<li><strong><a href="http://essaytyper.com/" target="_blank">EssayTyper</a> </strong> <br />
Need a BS essay? Input your topic, and watch the essay fly out as you type. 
</li>
<li><strong><a href="http://hackertyper.net/" target="_blank">HackerTyper</a> </strong> <br />
Need some BS code? Input your topic, and watch it fly out as you type. 
</li>
</ul>

