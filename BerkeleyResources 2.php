</li>
<li><strong><a href="http://courserank.com" target="_blank">CourseRank</a></strong>  <br />
  Like BerkeleyTime, but also has a 4-year planner, official and unofficial grade distributions, class reviews, and other cool things.<BR>
</li>
<li><strong><a href="http://www.ratemyprofessors.com/SelectTeacher.jsp?sid=1072" target="_blank">Rate My Professors</a></strong><BR>
Ratings for professors.</li>
<li><strong><a href="http://bearcheck.herokuapp.com/" target="_blank">BearCheck</a></strong> <br />
  Get notified when a seat opens up in a class you want! </li>
<li><strong><a href="https://kloudless.com/cal/" target="_blank">Kloudless</a></strong> <br />
  Does the same thing as BearCheck - notifies you of an open space in a class.</li>
<li><strong><a href="http://www.ocf.berkeley.edu/~tzhu/sections_updated.html" target="_blank">Has your section been updated recently? </a></strong> <br />
  A listing of sections that have been updated recently. </li>
<li><strong><a href="https://bcheck.hp.af.cm/" target="_blank">bCheck</a></strong> <br />
Check the status of a class (basically its listing in the Schedule of Classes) or find all open classes.
</li>
    <li><strong><a href="http://www.telebearsrtc.com/" target="_blank">Telebears RTC</a></strong><br>
A site that claims to be more reliable in showing current enrollment in a class than the UC Berkeley schedule/BearFacts/etc.</li>
<li><strong><a href="http://telefucked.com/" target="_blank">TeleFucked</a></strong> <br />
Got Telefucked and need to switch sections? Use this to find people to switch with.
</li>
<li><strong><a href="http://alex.heartdrops.org/cs.html" target="_blank">Cal Professor and Course Ratings Visualizer</a></strong><br>
Organizes ratings for professors into bubble nodes for visualization - kinda cool.</li>
</ul>


<a name="textbooks"><h3>Textbooks:</h3></a>
Hint: International versions, slightly older editions, and non-Berkeley versions should not be much different. If in doubt, ask people who have taken the class and/or your professor/GSI. Feel free to punch in your textbooks to all of these searches - they will probably show different prices from each other. 
<ul>
<li><strong>Facebook groups:</strong><BR>
  Direct selling/buying/trading between students, which means you get paid more for your books and you also pay less for your textbooks. 
  <ul>
  <li><a href="https://www.facebook.com/groups/266259890135558/" target="_blank">Textbook Exchange</a> <br /></li>
  <li><a href="https://www.facebook.com/groups/232773680090895/" target="_blank">UC Berkeley Textbooks</a> <br /></li>
  <li><a href="https://www.facebook.com/groups/337345069693706/" target="_blank">Textbook Trading</a> <br /></li>
  </ul>
</li>
<li><strong>Textbook Comparison Sites:</strong>
<ul>
<li><a href="http://bigwords.com" target="_blank"><strong>BigWords</strong></a>  <br />
  Personally my favorite site - it's a textbook comparison site. You can use this to find not only the cheapest textbooks to buy (including shipping and ALL of your textbooks together) but also the highest price (including shipping) for selling <i>back</i> your books as well. <BR>
</li>
<li><a href="http://slugbooks.com" target="_blank"><strong>Slugbooks</strong></a> <br />
  Another textbook comparison site. This one lets you fill in the classes and auto-populates the textbooks. <BR>
</li>
<li><a href="http://booksquad.com/" target="_blank"><strong>BookSquad</strong></a><br>
