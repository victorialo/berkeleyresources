<a name="tools"><h3>Handy Tools/Technology-Related:</h3></a>
<ul>
<li><strong><a href="https://idc.berkeley.edu/mmk/" target="_blank">Manage My Keys</a> </strong><BR>
Set your keys to use AirBears2 or bConnected on your devices. 
</li>
<li><strong><a href="https://net-auth.berkeley.edu/cgi-bin/krbsync" target="_blank">Synchronize CalNet PassPhrase</a> </strong><BR>
If your passphrases aren't synchronized, use this to fix it. 
</li>
<li><strong><a href="https://kb.berkeley.edu/campus-shared-services/" target="_blank">bConnected FAQ</a> </strong><BR>
Have any questions about using bConnected? Check here.
</li>
    <li><strong><a href="https://developer.berkeley.edu/" target="_blank">Berkeley API Central</a> </strong><BR>
Want to make a tool/web app for Berkeley students and need to pull data from Berkeley? Here's APIs for you to use.
</li>
<li><strong><a href="https://chrome.google.com/webstore/detail/berkeley-authentication-a/bnjahojpoacbilihlikppchnklabfmjp" target="_blank">Berkeley Authentication Autofiller</a> </strong> <br />
  Chrome extension to auto-fill in (or even auto-login!) your CalNet info for you so you don't have to login to AirBears or Telebears or bSpace or any of Berkeley's dozens of sites manually. THIS IS SUPER USEFUL. Only works on Chrome, though. <BR>
</li>
<li><strong>AirBears Supplicant</strong> (currently inactive until further notice)<br />
  Like the Berkeley Authentication Autofiller above, this logs you in automatically to AirBears and also comes in more versions. No longer in use/development due to AirBears 2, but left here in case the original AirBears returns. <br />
  <a href="https://github.com/nattofriends/airbears-supplicant" target="_blank">Windows (and looks like a Mac one is in progress too)</a> <br />
  <a href="https://github.com/kennydo/airbears-supplicant" target="_blank">Gnome (Linux - Fedora/Ubuntu)</a> <br />
  <a href="https://github.com/nol888/airbears-supplicant/downloads" target="_blank">Android</a> <br />
</li>
<li><strong><a href="https://github.com/edu" target="_blank">Github Educational Accounts</a></strong> <br />
  Github is a great tool to backup, collaborate, and merge code together. Anyone with a .edu email can get a free upgrade to a Micro plan, which includes free private repositories. 
</li>
    <li><strong><a href="http://szhu.me/subl" target="_blank">Install Sublime in Soda</a> </strong> <br />
  A super-awesome script to install Sublime Text editor on the lab machines in Soda. Woo, no more Emacs/Vi(m). <BR>
</li>
<li><strong><a href="https://www.rescuetime.com/ref/580690~" target="_blank">Rescuetime</a> </strong><BR>
Track how much time you're spending at your computer, and at which sites and which programs. Gives you a weekly report with exactly how many hours you spent at your computer, and your productivity score. Highly recommended. 
</li>
<li><strong><a href="http://bit.ly/Prezi4Cal" target="_blank">Prezi</a></strong> <br />
  Make animated, detailed infographics to give presentations from - basically like a really fancy slideshow. Sign up with your .edu email to get free edu membership, which removes the logo and gives you more storage space, in addition to some other features. <BR>
</li>
<li><strong><a href="http://bit.ly/LastPass4Cal" target="_blank">LastPass</a></strong> <br />
  Super-handy add-on to save your passwords on Firefox/Chrome/etc. Apparently having an .edu email gives you 6 free months of LastPass Premium as opposed to the free version. 
</li>
</ul>


