All the classes and their times.
</li>
<li><strong><a href="http://userscripts.org/scripts/show/105611" target="_blank">UC Berkeley Schedule Enhancer (UCBSE)</a></strong><br />
  Reorganizes and decorates the Berkeley Schedule of Classes and provides helpful links.<BR>
</li>
<li><strong><a href="http://decal.org/courses" target="_blank">DeCals</a></strong><br>
Student-run 1-2 unit P/NP classes. 
</li>
